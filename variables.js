// basic javaScript variables
// String variable
let name = "John Doe";
// Number variable
let age = 30;
// Boolean variable
let isEmployed = true;
// Null variable
let middleName = null;
// Undefined variable
let lastName;
// Symbol variable
let uniqueId = Symbol("id");
// BigInt variable
let bigNumber = BigInt(1234567890123456789012345678901234567890);
// Object variable
let person = {
    firstName: "John"
};
// Array variable
let numbers = [1, 2, 3, 4, 5];
// Function variable
function greet() {
    return "Hello!";
}
// Various data types
let dataTypes = {
    string: "This is a string",
    number: 42,
    boolean: false,
    nullValue: null,
    undefinedValue: undefined,
    symbolValue: Symbol("unique"),
    bigIntValue: BigInt(12345678901234567890),
    objectValue: { key: "value" },
    arrayValue: [1, 2, 3]
};
// various data types const, let, and var with comments and explanations
const constantValue = "This is a constant"; // const cannot be reassigned
let variableValue = "This is a variable"; // let can be reassigned
var globalValue = "This is a global variable"; // var can be redeclared and reassigned
// Example of using the variables
console.log(name); // Output: John Doe
console.log(age); // Output: 30
console.log(isEmployed); // Output: true
console.log(middleName); // Output: null